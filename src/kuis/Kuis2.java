    private void ftftgllahirFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_ftftgllahirFocusLost
        try {
        String tanggalLahirText = ftftgllahir.getText().trim();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate tanggalLahir = LocalDate.parse(tanggalLahirText, formatter);

        LocalDate sekarang = LocalDate.now();
        int umur = Period.between(tanggalLahir, sekarang).getYears();

        if (umur < 18) {
            JOptionPane.showMessageDialog(null, "Usia tidak memenuhi syarat. Minimal 18 tahun.");
            ftftgllahir.requestFocus(); 
        }
        } catch (DateTimeParseException e) {
            JOptionPane.showMessageDialog(null, "Format tanggal tidak valid. Gunakan format yyyy-MM-dd.");
            ftftgllahir.requestFocus(); 
        }
    }//GEN-LAST:event_ftftgllahirFocusLost

    private void BTNDaftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNDaftarActionPerformed
        if(tfnik.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "NIK Kosong");
        }
        if(tfnama.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama Kosong");
        }
        if(taalamat.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Alamat Kosong");
        }
        if(GENDERGROUP.getSelection()== null){
            JOptionPane.showMessageDialog(null, "Gender Kosong");
        }
        if(tftlp.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No Tlp.Kosong");
        }
        
        if(ftftgllahir.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Tanggal Lahir Kosong");
        }
        Object pendidikan = cbpendidikan.getSelectedItem();
        if(pendidikan == null){
            JOptionPane.showMessageDialog(null, "Pendidikan kosong");
        }
        if(ftfipk.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "IPK Kosong");
        }
        
        JOptionPane.showMessageDialog(null, "Pendaftaran Berhasil");
        
        tfnik.setText(""); 
        tfnama.setText(""); 
        taalamat.setText(""); 
        GENDERGROUP.clearSelection();
        tftlp.setText(""); 
        cbmembaca.setSelected(false);
        cbmenulis.setSelected(false);
        cbolahraga.setSelected(false);
        ftftgllahir.setText(""); 
        cbpendidikan.setSelectedIndex(-1); 
        ftfipk.setText(""); 
    }//GEN-LAST:event_BTNDaftarActionPerformed

