    private void tftlpKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tftlpKeyPressed
        char inputChar = evt.getKeyChar();
        if(!Character.isDigit(inputChar)&& inputChar != KeyEvent.VK_BACK_SPACE){
            evt.consume();
            JOptionPane.showMessageDialog(null, "input harus angka");
        }
    }//GEN-LAST:event_tftlpKeyPressed

    private void ftfipkFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_ftfipkFocusLost
        try {
        String inputText = ftfipk.getText().trim(); // Ambil teks penuh dari field

        if (!inputText.isEmpty()) {
            double ipk = Double.parseDouble(inputText);
            Object pendidikan = cbpendidikan.getSelectedItem();
            if (ipk < 0.00 || ipk > 4.00) {
                JOptionPane.showMessageDialog(null, "IPK tidak valid. Masukkan nilai antara 0.00 hingga 4.00.");
                ftfipk.requestFocus(); 
            } else if (ipk < 3.01 && "D1".equals(pendidikan)) {
                JOptionPane.showMessageDialog(null, "IPK tidak memenuhi minimal untuk pendidikan D1.");
                ftfipk.requestFocus(); 
            }
        } else {
            JOptionPane.showMessageDialog(null, "Masukkan nilai IPK.");
            ftfipk.requestFocus(); 
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "IPK yang dimasukkan tidak valid. Masukkan angka desimal.");
        ftfipk.requestFocus(); 
    }
    }//GEN-LAST:event_ftfipkFocusLost

